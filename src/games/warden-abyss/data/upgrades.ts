export interface Upgrade {
  id: string;
  nameKey: string;
  descriptionKey: string;
  level: number;
  maxLevel: number;
  baseCost: number;
  costMultiplier: number;
  isAuto: boolean;
  damageBonusPercent: number;
}

export const calculateUpgradeCost = (
  baseCost: number,
  multiplier: number,
  level: number
): number => {
  return Math.floor(baseCost * Math.pow(multiplier, level));
};

